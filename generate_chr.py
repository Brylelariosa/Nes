#!/usr/bin/env python3
def tile(b0, b1):
    return bytes(b0) + bytes(b1)

tiles = []
# 0: Air (empty)
tiles.append(tile([0]*8, [0]*8))
# 1: Dirt (brown pattern)
tiles.append(tile([0xFF,0x7F,0xFF,0xBF,0xFF,0x7F,0xFF,0xFF],
                  [0xFF,0xBF,0x7F,0xFF,0xFF,0xBF,0x7F,0xFF]))
# 2: Grass (green top)
tiles.append(tile([0x00,0x00,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF],
                  [0x00,0x00,0x7F,0xBF,0xFF,0x7F,0xBF,0xFF]))
# 3: Stone (gray noise)
tiles.append(tile([0xAA,0x55,0xAA,0x55,0xAA,0x55,0xAA,0x55],
                  [0x55,0xAA,0x55,0xAA,0x55,0xAA,0x55,0xAA]))
# 4: Wood (vertical grain)
tiles.append(tile([0x33,0x33,0x33,0x33,0x33,0x33,0x33,0x33],
                  [0xCC,0xCC,0xCC,0xCC,0xCC,0xCC,0xCC,0xCC]))
# 5-14: Empty
for _ in range(10):
    tiles.append(tile([0]*8, [0]*8))
# 15: Cursor (X shape)
tiles.append(tile([0x81,0x42,0x24,0x18,0x18,0x24,0x42,0x81],
                  [0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00]))
# Pad to 128 tiles (2KB)
while len(tiles) < 128:
    tiles.append(tile([0]*8, [0]*8))

with open("game.chr", "wb") as f:
    for t in tiles:
        f.write(t)
